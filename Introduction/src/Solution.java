import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

	public static void main(String[] args) {

		Scanner in = new Scanner(System.in);

		int lineCounter =0;
		while (in.hasNext()) {
			lineCounter++;
			String a = in.nextLine();
			System.out.println(lineCounter+" " +a);
		}

		/*
		 * Read input from STDIN (std input). Print output to STDOUT. Your class should
		 * be named Solution.
		 */
	}
}
